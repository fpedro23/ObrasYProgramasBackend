package modelObrasYProgramas;

public class Inaugurador
{
    private String idCargoInaugura;
    private String nombreCargoInaugura;
	/**
	 * @return the idCargoInaugura
	 */
	public String getIdCargoInaugura() {
		return idCargoInaugura;
	}
	/**
	 * @param idCargoInaugura the idCargoInaugura to set
	 */
	public void setIdCargoInaugura(String idCargoInaugura) {
		this.idCargoInaugura = idCargoInaugura;
	}
	/**
	 * @return the nombreCargoInaugura
	 */
	public String getNombreCargoInaugura() {
		return nombreCargoInaugura;
	}
	/**
	 * @param nombreCargoInaugura the nombreCargoInaugura to set
	 */
	public void setNombreCargoInaugura(String nombreCargoInaugura) {
		this.nombreCargoInaugura = nombreCargoInaugura;
	}
    
}