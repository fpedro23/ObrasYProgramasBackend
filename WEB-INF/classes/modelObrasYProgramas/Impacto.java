package modelObrasYProgramas;
import java.util.*;

public class Impacto
{
    private String idImpacto;
    private String nombreImpacto;
	/**
	 * @return the idImpacto
	 */
	public String getIdImpacto() {
		return idImpacto;
	}
	/**
	 * @param idImpacto the idImpacto to set
	 */
	public void setIdImpacto(String idImpacto) {
		this.idImpacto = idImpacto;
	}
	/**
	 * @return the nombreImpacto
	 */
	public String getNombreImpacto() {
		return nombreImpacto;
	}
	/**
	 * @param nombreImpacto the nombreImpacto to set
	 */
	public void setNombreImpacto(String nombreImpacto) {
		this.nombreImpacto = nombreImpacto;
	}
    
}