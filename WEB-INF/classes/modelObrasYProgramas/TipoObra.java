package modelObrasYProgramas;
import java.util.*;

public class TipoObra
{
    private String idTipoObra;
    private String nombreTipoObra;
	/**
	 * @return the idTipoObra
	 */
	public String getIdTipoObra() {
		return idTipoObra;
	}
	/**
	 * @param idTipoObra the idTipoObra to set
	 */
	public void setIdTipoObra(String idTipoObra) {
		this.idTipoObra = idTipoObra;
	}
	/**
	 * @return the nombreTipoObra
	 */
	public String getNombreTipoObra() {
		return nombreTipoObra;
	}
	/**
	 * @param nombreTipoObra the nombreTipoObra to set
	 */
	public void setNombreTipoObra(String nombreTipoObra) {
		this.nombreTipoObra = nombreTipoObra;
	}
    
}